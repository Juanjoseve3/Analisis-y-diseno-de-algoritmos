def solucionar_n_reinas_util(tablero, fila):
    Reynas = len(tablero)
    if fila == Reynas:
        return True
    for columna in range(Reynas):
        if es_seguro(tablero, fila, columna):
            tablero[fila] = columna
            if solucionar_n_reinas_util(tablero, fila + 1):
                return True
            tablero[fila] = -1
    return False

